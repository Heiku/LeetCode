public class ByteDance_Fourth {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        while (sc.hasNext()){
            int n = sc.nextInt();

            int[] arr = new int[n];
            for (int i = 0; i < n; i++)
                arr[i]= sc.nextInt();


            System.out.println(0);
        }
    }
}
